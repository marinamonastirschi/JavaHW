package class7;

public class HW2 {
    public static void main(String[] args) {
        int counter = 100;
        while (counter > +1) {
            System.out.print(counter + " ");
            counter--;
        }

        System.out.println();
        for (counter = 100; counter >= 1; counter--) {
            System.out.print(counter + " ");
        }
    }
}
