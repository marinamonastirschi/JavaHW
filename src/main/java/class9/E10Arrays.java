package class9;

public class E10Arrays {
    public static void main(String[] args) {
        int[] numbers={6,5,4,3,2,1,0};
        for (int i = 0; i < numbers.length; i++) {
            System.out.print(numbers[i]+ " ");

        }
        char[] chars={'A','B','C','D','E','F'};
        for (int i = 5; i < 0; i--) {
            System.out.println(chars[i]);

        }
    }
}
