package class7;

public class HW1 {
    public static void main(String[] args) {
        int counter = 1;
        while (counter <= 100) {
            System.out.print(counter + " ");
            counter++;
        }
        counter = 1;
        System.out.println();
        for (int i = 1; i <= 100; i++) {
            System.out.print(i + " ");
        }
    }
}
