package class7;

public class HW3 {
    public static void main(String[] args) {
        int i = 20;
        while (i >= 1) {
            System.out.print(i + " ");
            i -= 2;
        }
        System.out.println();
        for (i = 20; i >= 1; i -= 2) {
            System.out.print(i + " ");
        }
    }
}
